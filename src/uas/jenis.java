    public void tampilan() {
        try {
            koneksi = conn.bukainkoneksi();
            st = koneksi.createStatement();
            rs = st.executeQuery("SELECT * FROM tbljenis");
            while (rs.next()) {
                String[] baris = {rs.getString(1), rs.getString(2)};
                model.addRow(baris);
            }
            tabel.setModel(model);

        } catch (SQLException e) {
            System.out.println("Koneksi Error Bambank!" + e);
        }
    }
   
    public void simpan() {
        String kode = tkode.getText();
        String jenis = tjenis.getText();

        if (kode.equals("") || jenis.equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua form!");
        } else {
            String sql = "insert into tbljenis(kodej, jenis)"
                    + "values"
                    + "('" + kode + "', "
                    + "'" + jenis + "'"
                    + ")";
            conn.simpanData(sql);
        }

        new jenis().setVisible(true);
        this.dispose();
    }

    public void edit() {
        String kode = tkode.getText();
        String jenis = tjenis.getText();

        if (kode.equals("") || jenis.equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua form!");
        } else {
            String sql = "Update tbljenis set jenis = '" + jenis + "' where kodej = '" + kode + "' ";
            conn.simpanData(sql);
        }

        new jenis().setVisible(true);
        this.dispose();
    }

    public void hapus() {
        String kode = tkode.getText();
        String jenis = tjenis.getText();
        if (kode.equals("") || jenis.equals("")) {
            JOptionPane.showMessageDialog(this, "Isi semua form!");
        } else {
            String sql = "Delete From tbljenis where kodej = '" + kode + "' ";
            conn.simpanData(sql);
        }
        new jenis().setVisible(true);
        this.dispose();
    }

